let percentage = 65
if (percentage>= 90){
    console.log ('A1 grade')
} 
else if(percentage>=80 && percentage<=89.99){
    console.log ('A grade')
}
else if(percentage>=70 && percentage<=79.99){
    console.log ('B grade')
}
else if(percentage>=60 && percentage<=69.99){
    console.log ('C grade')
}
else if(percentage>=50 && percentage <=59.99){
    console.log ('D grade')
}
else{
    console.log ('Fail')
}